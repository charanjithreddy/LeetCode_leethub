class Solution(object):
    def totalDistance(self, s):
        d={'q':[0,0],'w':[0,1],'e':[0,2],'r':[0,3],'t':[0,4],'y':[0,5],'u':[0,6],'i':[0,7],'o':[0,8],'p':[0,9],'a':[1,0],'s':[1,1],'d':[1,2],'f':[1,3],'g':[1,4],'h':[1,5],'j':[1,6],'k':[1,7],'l':[1,8],'z':[2,0],'x':[2,1],'c':[2,2],'v':[2,3],'b':[2,4],'n':[2,5],'m':[2,6]};
        x=d['a'][0];
        y=d['a'][1];
        res=0;
        for i in s:
            res+=abs(d[i][0]-x);
            res+=abs(d[i][1]-y);
            x=d[i][0]
            y=d[i][1]
        return res
        """
        :type s: str
        :rtype: int
        """